"""
Коды качества значений тегов.

См. также документацию в ``docs/source/historical_data.rst``.
"""

# 0 (или null) — обычное «хорошее» значение.
CN_QUALITY_GOOD = 0

# 100 — разорвана связь между платформой и коннектором.
CN_QUALITY_CONNECTION_LOST = 100

# 101 — установлена связь между коннектором и платформой (восстановление после 100),
# используется вместе со значением null.
CN_QUALITY_CONNECTION_RESTORED = 101

# 102 — разорвана связь между коннектором и поставщиком данных.
# Этот код пишет коннектор, платформа сама его не генерирует.
CN_QUALITY_SOURCE_DISCONNECTED = 102

# 103 — плохое качество данных от источника данных.
# Этот код пишет коннектор, платформа сама его не генерирует.
CN_QUALITY_SOURCE_BAD_DATA = 103

